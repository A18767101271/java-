package kotlin_.system_practise

/**
 * 字符串类型转换基本数据类型等
 * Created by wislie on 2019/1/18.
 */

fun main(args: Array<String>) {


    val charStr = "mediocrity"
    println(charStr.toCharArray())

    val intStr = "1024"
    println(intStr.toInt())

    val floatStr = "25.6"
    println(floatStr.toFloat())

    val doubleStr = "1.28"
    println(doubleStr.toDouble())

    val boolStr = "false"
    println(boolStr.toBoolean())


}